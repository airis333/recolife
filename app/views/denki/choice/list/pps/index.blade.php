      <h3 class="report__subttl">新電力（PPS）とは</h3>
      <p class="report__details">新電力（PPS）とは、Power Producer and Supplierの略で、大手電力会社以外で企業や店舗向けに電力を供給している企業のことです。<br>電力自由化は、一般家庭向けには2016年からのスタートですが、企業・店舗向けにはすでにスタートしていました。そこで電力供給の実績があるのが、新電力（PPS）と呼ばれる企業なのです。</p>
      <h3 class="report__subttl">新電力（PPS）の特徴・メリットは</h3>
      <p class="report__details">一般家庭向けがスタートする前から、いち早く電力自由化に参入していた新電力（PPS）企業。電力関連企業の子会社や関連企業が多いのが特徴です。<br>メリットは、企業向けにすでに大規模の電力を供給している実績で、安定的に電力が供給される安心感があります。</p>
