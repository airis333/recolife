        <h2 class="report__ttl">電力自由化　私たちへのメリット</h2>
        <p class="report__overview">電力自由化が始まることで、私たちの生活にはどのようなメリットがあるのでしょうか。<br>メリットを最大限に活用して、電気代を節約しましょう。</p>
