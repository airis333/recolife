      <h3 class="report__subttl">競争で電気料金が下がる</h3>
      <p class="report__details">電力自由化が始まり、これまで「東京電力」や「関西電力」など、各地域の電力会社が独占していた電力市場が開放されることになりました。<br>市場が開放されたことで、自然と他社との競争が働き、全体的に電気料金プランが安くなる傾向に向かうだろうと言われています。電力市場全体で料金が安くなるのは一般家庭にとって嬉しいメリットです。</p>
      <h3 class="report__subttl">安価な電力会社を選べる</h3>
      <p class="report__details">自分で電力会社を選択できるようになったことで、大手電力会社より安価な電力会社と契約出来るのが、電力自由化の大きなメリットです。<br>実際、地域の電力会社から乗り換えることで電気料金が下がった事例が生まれています。<br>家庭向けの電力に先駆けて、工場や学校向けに2000年3月から導入されていた電力自由化。愛媛県の中学校では、四国電力から新規参入企業へ乗り換えた結果、年間で約6.2％、金額にすると500万円のコスト削減した実績があります。</p>
      <h3 class="report__subttl">セット割など新たなサービスが生まれる</h3>
      <p class="report__details">電力事業への参入業者はさまざまで、都市ガス大手の東京ガスや、家電量販店のヤマダ電機、私鉄の東急電鉄など、業種もかなり幅広いです。<br>各業者がそれぞれの強みを生かし、例えば「電気とガスのセット割引」「買い物で使えるポイントが貯まる」など、今までになかった特典が生まれています。<br>自分のライフスタイルに合ったサービスを選べば、新たな節約ができるチャンスとなり、これも電力自由化の魅力的なメリットといえます。</p>
      <h3 class="report__subttl">再生可能エネルギーが選択できる</h3>
      <p class="report__details">東電の福島第一原発事故が発生して以来、原子力発電への批判が高まっています。「再生可能エネルギーの電気だけを使いたい」というニーズに応えられるのも、電力自由化のメリットの1つです。<br>実際に、都内のエネルギー流通業者のエネリスは、環境意識の高い消費者向けに再生可能エネルギーのみで発電した電気の販売を検討していると発表しています。<br>意識的に再生可能エネルギーを選ぶ消費者には嬉しい動きです。</p>
      <h3 class="report__subttl">電力自由化にはデメリットも</h3>
      <p class="report__details">私たちにとってメリットが大きいと思われる電力自由化ですが、デメリットがないわけではありません。それは電力の安定供給という点です。<br>電力自由化が始まる前、専売的に電気を販売していた大手電力会社は、もちろん電力事業が本業のため、電力を安定的に供給する設備や技術が十分にありました。<br>新規参入する企業では設備や技術とともに、災害対応のノウハウが不十分な可能性もあり、大規模な停電が発生しやすいのでは、といわれています。<br>電気事業者選びの際は、「もしもの時に電力を安定して供給できるか」という点にも注目する必要がありそうです。</p>
